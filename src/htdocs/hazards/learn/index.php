    <h3>If places on the map have the same color, will they have the same level of damage if an earthquake occurs?</h3>
    <p>
      Not necessarily.  Areas with the same color on the map should expect a similar <strong>number of occurrences</strong> of damaging earthquake shaking. However, the <strong>level of damage</strong> caused by the ground shaking associated with each earthquake could be very different. For example, a smaller earthquake that produces some damage over a smaller area, and a larger earthquake that causes widespread damage, are both counted in occurrences of damaging earthquake shaking.
    </p>
  </section>
  <section class="column one-of-three">
    <h3>Why do I need to worry about the earthquakes on this map?  10,000 years seems like a very long time.</h3>
    <p>
      This map shows how many times earthquakes could cause damaging ground shaking in 10,000 years and yes, that is a very long time! But that doesn't mean the earthquakes are guaranteed to be far in the future. They could happen at any time, including today. Scientists look at earthquakes over a long time period to get a fuller picture of earthquake hazard.
    </p>
  </section>
