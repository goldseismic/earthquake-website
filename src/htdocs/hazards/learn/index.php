    <h3>I live in a grey area on the map.  Does that mean there is no chance of damaging shaking from an earthquake in my area?</h3>
    <p>
      No, that is not what the gray areas mean. There is a chance of damaging shaking anywhere and everywhere in the United States.  Most people pay particular attention to areas with the hottest colors. True, these areas will most often have damaging shaking, but don't ignore the cooler shades. Damaging shaking can and will happen in those areas, too, but less often. In fact, damaging shaking is possible in all fifty states. The cooler color areas, like grey, are low hazard but not <em>no</em> hazard.
    </p>
  </section>
  <section class="column one-of-two">
    <h3>What are the areas in black outlines?</h3>
    <p>
      Notice that in some states there are black outlines. These show areas where the number of occurrences of damaging shaking could go up because recently there have been "induced" earthquakes in these locations. <strong>Induced</strong> earthquakes are caused by human activity while <strong>natural</strong> earthquakes are caused by geologic processes. The colors on this map only show the hazard from natural earthquakes. In the areas with induced earthquakes, the number of occurrences of damaging shaking could be much higher than the value shown on this map, and could drop lower again if the human activities that are causing the earthquakes change. The USGS made another map that includes <a href="/hazards/induced/">induced earthquakes</a>.
    </p>
