    <h3>What’s the difference between geologic hazard and risk?</h3>

    <p>Geologic <strong>hazards</strong> are naturally-occurring phenomena capable of causing loss or damage. <strong>Risk</strong> is the potential that exposure to the hazard will lead to a negative consequence such as loss of life or economic loss. To illustrate the difference, consider the following <strong>two examples</strong>.
    </p>
    <ol>
      <li>
        California has a number of faults that produce earthquakes during any given year.  Some of these faults are located in populated regions, putting the people in those areas at risk.  Other faults occur in remote areas where few people live and few structures exist.  While the hazard may be the same for the two different areas, the regional risk differs because the potential impact is greater in the more populated area.
      </li>
      <p></p>
      <li>
        Town A and Town B are right next to the same earthquake fault. They have the same earthquake hazard. But they don't have the same risk, because Town A has buildings built to withstand earthquake ground shaking and Town B does not. If you are Town B, you have two things you can do to reduce your risk of being hurt or killed in an earthquake. You can build to withstand earthquake ground shaking. Or you can move.
      </li>
    </ol>
      <p>
        <strong>Bottom line:</strong> High earthquake hazard does not mean high risk.  Also, while you can’t change the underlying earthquake hazard, you can reduce earthquake risk by managing the built environment.
      </p>
  </section>

  <section class="column one-of-two">
    <h3>What are the USGS National Seismic Hazard Maps?</h3>

    <p>
      The National Seismic Hazard Maps (NSHM) (and the hazard model from which they are derived) are first and foremost a suite of products aimed at improving earthquake-resilient construction in the United States.  Engineers who construct buildings need to know how strongly a particular site might be shaken by earthquakes. The NSHMs address this question by compiling all known earthquake sources (and proxies for unknown sources!), their distance from the site in question, and other seismological and geological information to project potential maximum expected ground motions at a site over a particular period of time (say 50 years). These estimates are computed for hundreds of thousands of evenly distributed sites, and the information is then summarized into a series of maps.
    </p>
    <p>
      Different maps portray different types of ground shaking information; some maps are designed to inform engineers on the design of small residential structures affected by high frequency ground motion, others are useful for designing tall buildings and long bridges that are more susceptible to longer wavelength ground shaking. The NSHMs (and underlying model) are updated every six years to provide the basis for earthquake provisions in building codes. Regular updates ensure that engineers have access to the most accurate and up-to-date information about potentially damaging earthquakes throughout the United States.
    </p>
  </section>
