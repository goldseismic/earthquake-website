<h3>Note 1:</h3>

<p>
Because the network has evolved over the years, there is no guarantee of consistency in detection threshold, magnitude determination or region covered. The data in this catalog come from numerous sources:
<ul>
  <li>1974-1992: TEIC earthquake bulletin and St Louis Regional Catalog </li>
  <li>1993-1994: Saint Louis University and Central Mississippi Valley Earthquake Bulletins </li>
  <li>1995-present: earthquakes reported by SLU and/or CERI </li>
  <li>2003-present: addition of CERI East TN earthquakes and contributions from the SEUSSN bulletins</li>
</ul>

